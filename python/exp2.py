a = 10
b = 20
print("RELATIONAL OPERATOR")
print("a == b:", a == b)
print("a != b:", a != b)
print("a < b:", a < b)
print("a > b:", a > b)
print("a <= b:", a <= b)
print("a >= b:", a >= b)


print("\nLOGICAL OPERATOR")
print("((a < b) and (b >= a)):", (a < b) and (b >= a))
print("((a > b) or (b >= a)):", (a > b) or (b >= a))
print("not(b < a):", not(b < a))

print("\nMEMBERSHIP OPERATOR")
a = "h"
b = "hello"
print("h in hello:", a in b)
print("h not in hello:", a not in b)


print("\nIDENTITY OPERATOR")
a = "h"
b = "hello"
print("h is hello:", a is b)
print("h not is hello:", a is not b)


a = 10
b = 5
print("\nBITWISE OPERATOR")
print("a & b:", a & b)
print("a | b:", a | b)
print("a ^ b:", a ^ b)
print("~a:", ~a)
print("a << b:", a << b)
print("a >> b:", a >> b)
print("-(a):", -(a))
print("~(-a):", ~(a))


